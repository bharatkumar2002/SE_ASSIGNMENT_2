#include <stdio.h>

void main() 
{ 
    // Declare variables
    int age = 30; 
    float height = 5.7;
    char initial = 'J';

    // Declare a constant
    const int birthYear = 1995;

    // Print values of variables
    printf("Age: %d\n", age); 
    printf("Height: %.1f feet\n", height);
    printf("Initial: %c\n", initial);
    printf("Birth Year: %d\n", birthYear);
}

